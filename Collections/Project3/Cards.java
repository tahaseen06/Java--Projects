package cards;

public class Cards {
	 private String symbol;
	    private int number;

	    public Cards(String symbol, int number) {
	        this.symbol = symbol;
	        this.number = number;
	    }

	    public String getSymbol() {
	        return symbol;
	    }

	    public int getNumber() {
	        return number;
	    }
}
